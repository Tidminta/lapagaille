#include "libft.h"
#include <stdlib.h>

char	*ft_strdup(const char *s1)
{
	char	*tab;
	int		len;
	int		i;

	len = 0;
	while (s1[len] != '\0')
		len++;
	tab = (char *)malloc(sizeof(char) * (len + 1));
	if (!tab)
		return (NULL);
	i = 0;
	while (s1[i] != '\0')
	{
		tab[i] = s1[i];
		i++;
	}
	tab[i] = '\0';
	return (tab);
}
